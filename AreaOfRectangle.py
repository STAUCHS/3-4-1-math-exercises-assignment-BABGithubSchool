length = float(input("Enter the length (cm):"))
width = float(input("Enter the width (cm):"))
area = (float(length)*float(width))
print("The area of your rectangle is", area, "cm^2.")
print("The area of your rectangle is " + str(area) + " cm^2.")
print(f"The area of your rectangle is {area} cm^2.")
